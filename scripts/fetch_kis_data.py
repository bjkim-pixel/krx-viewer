def fetch_price(token, code):
    d = kis_get("/uapi/domestic-stock/v1/quotations/inquire-price",
        {"FID_COND_MRKT_DIV_CODE": "J", "FID_INPUT_ISCD": code},
        "FHKST01010100", token)
    o = d.get("output", {})
    if not o: return {}
    price  = si(o.get("stck_prpr", 0))
    high52 = si(o.get("d250_hgpr", 0))
    ratio  = round(price / high52 * 100, 1) if high52 else 0
    flag   = "신고가" if ratio>=100 else "99%" if ratio>=99 else "97%+" if ratio>=97 else ""
    return {
        "is52h":  ratio >= 100,
        "isAllH": o.get("new_hgpr_lwpr_cls_code") == "1",
        "nh_ratio": ratio, "nh_flag": flag,
        "dept": o.get("bstp_kor_isnm", "").strip(),
    }


