    all_stocks = []
    for code, v in result.items():
        if not v: continue
        cap  = v.get("cap", 0)
        frgn = v.get("frgn")
        inst = v.get("inst")
        priv = v.get("priv")
        all_stocks.append({
            "code":     code,
            "name":     v.get("name", code),
            "market":   v.get("market", ""),
            "cap":      cap,
            "dept":     v.get("dept", ""),
            "frgn":     frgn,
            "inst":     inst,
            "priv":     priv,
            "f_consec": v.get("f_consec", 0),
            "i_consec": v.get("i_consec", 0),
            "is52h":    v.get("is52h", False),
            "isAllH":   v.get("isAllH", False),
            "nh_flag":  v.get("nh_flag", ""),
            "fi_sum":   (frgn or 0) + (inst or 0),
        })

    # ── 4조↑ 종목 (메시지1/2/3/4용) ─────────────────────────
    stocks_4t = [s for s in all_stocks if s["cap"] >= 4_000_000_000_000]
    stocks_4t.sort(key=lambda x: x["fi_sum"], reverse=True)

    # ── 비중 계산 헬퍼 ────────────────────────────────────────
    def pct_of_cap(amt, cap):
        """순매수금액 / 시가총액 × 100"""
        if cap <= 0 or amt is None: return 0.0
        return round(abs(amt) / cap * 100, 2)

    messages = []

    # ── 메시지 1: 4조↑ 외국인+기관 순매수 TOP20 ──────────────
    rows = []
    for i, s in enumerate(stocks_4t[:20], 1):
        mk  = "K" if s["market"] == "KOSPI" else "Q"
        nh  = f" 🚀{s['nh_flag']}" if s.get("nh_flag") else ""
        fc  = f" 외{s['f_consec']}일" if s["f_consec"] >= 3 else ""
        ic  = f" 기{s['i_consec']}일" if s["i_consec"] >= 3 else ""
        rows.append(
            f"{i:2d}. <b>{s['name']}</b>[{mk}]{nh}\n"
            f"    외인 <b>{fmt_amt(s['frgn'])}</b>  기관 <b>{fmt_amt(s['inst'])}</b>"
            f"  시총{fmt_cap(s['cap'])}{fc}{ic}"
        )
    msg1 = (
        f"📊 <b>KRX Analyzer  |  {now_str}</b>\n"
        f"{'━'*28}\n"
        f"<b>시총4조↑  외국인+기관 순매수 TOP20</b>\n\n"
        + "\n".join(rows)
    )
    messages.append(msg1)

    # ── 메시지 2: 외국인 전체 매수TOP20 / 매도TOP20 (비중포함) ──
    f_buy  = sorted([s for s in all_stocks if (s["frgn"] or 0) > 0],
                    key=lambda x: x["frgn"], reverse=True)[:20]
    f_sell = sorted([s for s in all_stocks if (s["frgn"] or 0) < 0],
                    key=lambda x: x["frgn"])[:20]

    def rank_rows(lst, key):
        lines = []
        for i, s in enumerate(lst, 1):
            v   = s[key] or 0
            pct = pct_of_cap(v, s["cap"])
            mk  = "K" if s["market"] == "KOSPI" else "Q"
            fc  = f" {s['f_consec']}일↑" if key=="frgn" and s["f_consec"]>=2 else ""
